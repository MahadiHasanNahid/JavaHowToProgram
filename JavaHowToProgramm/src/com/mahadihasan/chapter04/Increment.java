package com.mahadihasan.chapter04;

/**
 *
 * @author Md Mahadi Hasan Nahid
 */
public class Increment {
    
    public static void main( String[] args ) {
        int c;
        
        c = 5;
        System.out.println( c );
        System.out.println( c++ );
        System.out.println( c );
        
        System.out.println();
        
        c = 5;
        System.out.println( c );
        System.out.println( ++c );
        System.out.println( c );
    }
}
